@Adyan.ar_cmd(
    pattern="ك1$",
    command=("ك1", plugin_category),
           )
async def tmgif(event):
    if event.fwd_from:
        return
    ROZ = await reply_id(event)
    if cute:
        Adyan = f"**{ROZA}**\n"
        Adyan += f"✛━━━━━━━━━━━━━✛\n"
        Adyan += f"**الـمتحـرڪـة الأولـى **"
        await event.client.send_file(event.chat_id, cute, caption=Adyan, reply_to=ROZ)

